package class02;

public class AssignmentOperator {

	public static void main(String[] args) {
		int sal=5000;
		// Add asignment +=
		sal+=4000; // sal=sal+4000
		System.out.println("Current Salary"+sal);
		//sustract assignment -=
		sal-=2000; // sal=sal-2000
		System.out.println("Current Salary"+sal);
		//mulitply assignment *=
		sal*=5; // sal=sal*5
		System.out.println("Current Salary"+sal);
		//division assignment /=
		sal/=5; // sal=sal/5
		System.out.println("Current Salary"+sal);
		//Mudulo assignment %=
		sal%=7; // sal=sal%2000
		System.out.println("Current Salary"+sal);
	}

}
